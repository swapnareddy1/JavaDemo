package naveenjava;

public class Vowels {

	public static void main(String[] args) {
		char ch='a';
		//System.out.println(i);
		//for(char ch='a';ch<='z';ch++) 
		while(ch<='z')
		{
			//System.out.println(i);
			switch(ch) 
			{
			case 'a':
				System.out.println(ch);
				break;
			case 'e':
				System.out.println(ch);
				break;
			case 'i':
				System.out.println(ch);
				break;
			case 'o':
				System.out.println(ch);
				break;
			case 'u':
				System.out.println(ch);
				break;
				
			default:
					break;
				
			}
			ch++;
	}

}
}
